    private void TablaMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_TablaMouseClicked
        try{
            int fila=Tabla.getSelectedRow();
            int id= Integer.parseInt((Tabla.getValueAt(fila,0).toString()));
            PreparedStatement ps;
            ResultSet rs;
            conexion=adaptador.Conexion();
            ps=adaptador.Conexion().prepareStatement("select nit,nombre,apellidos,edad,sexo,direccion from cliente where id=?");
            ps.setInt(1, id);
            rs=ps.executeQuery();
            while(rs.next()){
                txtid.setText(String.valueOf(id));
                txtnit.setText(rs.getString("nit"));
                txtnombre.setText(rs.getString("nombre"));
                txtapellido.setText(rs.getString("apellidos"));
                txtedad.setText(rs.getString("edad"));
                jComboBoxSexo.getSelectedItem().toString();
                txtdireccion.setText(rs.getString("direccion"));
                
            }
            
            
        }catch(Exception e){
            System.out.println(e);
        }
    }//GEN-LAST:event_TablaMouseClicked

