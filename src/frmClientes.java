void consulta(){
      try{
           ArrayList<Cliente> clientes=repositorio.read();
           
           for(Cliente cliente:clientes){
               Object[] fila= new Object[7];
               fila[0]=cliente.getId();
               fila[1]=cliente.getNit();
               fila[2]=cliente.getNombre();
               fila[3]=cliente.getApellidos();
               fila[4]=cliente.getEdad();
               fila[5]=cliente.getSexo();
               fila[6]=cliente.getDireccion();
               modelo=(DefaultTableModel)Tabla.getModel();
               modelo.addRow(fila);
           }
       }catch(Exception e){
           System.out.println(e);
       }
}
